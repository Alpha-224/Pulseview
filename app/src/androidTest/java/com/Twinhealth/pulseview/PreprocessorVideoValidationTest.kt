/**
 * Instrumented validation test: Kotlin Preprocessor vs. Python reference pipeline.
 *
 * This test decodes exactly the first 21 frames of the real rPPG test video via
 * Android's MediaMetadataRetriever, runs them through the same Preprocessor pipeline
 * (detectFaceBox → cropAndResize → diffNormalize), extracts the same probe values
 * produced by the Python reference script (gen_reference.py), and asserts they match
 * within a tolerance of 0.01.
 *
 * WHY 0.01 TOLERANCE:
 *   Python and Kotlin now share the exact same pixel data (PNG frames extracted
 *   from the reference video via Python's cv2.VideoCapture — same FFmpeg decoder
 *   used by gen_reference.py). PNG is lossless, so the Android test loads
 *   bit-for-bit identical pixel values. The remaining tolerance covers:
 *     - Float32 arithmetic differences between NumPy and Kotlin/JVM (< 1e-5)
 *     - INTER_AREA resize rounding between OpenCV Python and Android AAR (< 0.005)
 *   A tolerance of 0.01 comfortably covers these while catching real bugs
 *   (e.g., BGR/RGB swap ≈ 0.03–0.2 error).
 *
 * WHY PNG FRAMES INSTEAD OF VIDEO:
 *   Decoding H.264 with Python's FFmpeg vs Android's MediaCodec applies different
 *   YUV→RGB color matrices (BT.601 vs BT.709). This causes a systematic ~7 DN
 *   difference in R channel (Δ≈0.027 at pixel 0,0), which far exceeds the 0.01
 *   tolerance and is NOT a bug in Preprocessor. By extracting the 21 frames as
 *   lossless PNGs using the same Python/FFmpeg decoder, we make the test measure
 *   only the preprocessing math, not the decoder.
 *
 * IMPORTANT DISCREPANCY NOTE — diffNormalize divisor:
 *   Python gen_reference.py uses: diff / (np.std(diff) + 1e-7)
 *   Kotlin Preprocessor.diffNormalize uses: diff / std  (no +1e-7 in divisor)
 *   For real video data where std >> 1e-7, the difference is negligible (< 1e-6).
 *   The test probes diffnorm_global_std_used which reflects np.std(diff) before
 *   the +1e-7 addition, so the comparison is direct.
 *
 * IMPORTANT DISCREPANCY NOTE — face selection and cascade coordinate jitter:
 *   Python gen_reference.py uses faces[0] (first detected face by cascade order).
 *   Kotlin Preprocessor.detectFaceBox uses maxByOrNull { it.width } (largest face).
 *   Additionally, the cascade classifier can return coordinates that differ by
 *   a few pixels between desktop OpenCV (used by Python, SIMD-optimized for x86)
 *   and the Android AAR (ARM NEON path). Observed: Python x=84, Kotlin x=87 (Δ=3px).
 *
 *   STRATEGY: We log the detected Kotlin face box as a diagnostic but do NOT hard-
 *   assert it against the Python reference. Instead, for pixel probe comparisons
 *   (frame0_pixel0_rgb, diffnorm_frame5_pixel100_rgb, etc.) we force-use the Python
 *   reference face box [84, 181, 345, 345] for cropAndResize/diffNormalize. This
 *   isolates the crop+diffnorm math from cascade coordinate jitter, giving a clean
 *   apples-to-apples comparison of the preprocessing math itself.
 *
 * CHANNEL ORDER NOTE:
 *   Python saves frames via cv2.imwrite(path, bgr_frame): PNG stores RGB.
 *   Android loads via BitmapFactory.decodeStream: gives ARGB_8888 Bitmap (visual RGB).
 *   Utils.bitmapToMat: gives BGRA Mat (OpenCV convention for ARGB_8888 input).
 *   Imgproc.cvtColor(BGRA2RGB): gives 3-channel RGB Mat.
 *   This exactly mirrors Python's cvtColor(bgr_frame, BGR2RGB) — byte-for-byte.
 *
 * REQUIRES: Physical Android device connected via ADB with USB debugging enabled.
 *           Frame assets: app/src/androidTest/assets/frames/frame_NN.png
 *             (21 files, NN = 00..20, extracted by gen_reference.py's decoder)
 *           Haar cascade: app/src/main/res/raw/haarcascade_frontalface_default.xml
 */
@RunWith(AndroidJUnit4::class)
class PreprocessorVideoValidationTest {

