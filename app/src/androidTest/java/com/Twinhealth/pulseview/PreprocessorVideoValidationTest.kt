        private val REF_FACE_BOX = intArrayOf(84, 181, 345, 345)  // [x, y, w, h]

        private val REF_FRAME0_PIXEL0_RGB = floatArrayOf(
            0.9176470637321472f,
            0.8980392217636108f,
            0.886274516582489f
        )
        private val REF_DIFFNORM_FRAME5_PIXEL100_RGB = floatArrayOf(
            -0.27356529235839844f,
            -0.2857840955257416f,
            -0.2975994646549225f
        )
        private val REF_DIFFNORM_FRAME19_PIXEL0_RGB = floatArrayOf(0.0f, 0.0f, 0.0f)
        private val REF_DIFFNORM_FRAME20_PIXEL0_RGB = floatArrayOf(0.0f, 0.0f, 0.0f)
        private const val REF_GLOBAL_STD = 0.9999885559082031f
        private const val REF_RAW_FRAMES = 21

        // Tolerance for all floating-point comparisons.
