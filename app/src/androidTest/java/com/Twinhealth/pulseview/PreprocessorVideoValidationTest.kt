    @Test
    fun validatePreprocessorAgainstPythonReference() {
        // ── Step 1: Load 21 pre-extracted PNG frames ──────────────────────────
        // Frames were extracted from the reference video using the SAME Python/FFmpeg
        // decoder as gen_reference.py (cv2.VideoCapture), saved as lossless PNGs.
        // This eliminates the BT.601 vs BT.709 decoder difference that causes
        // a ~7 DN systematic error in R when decoding H.264 via Android MediaCodec.
        Log.i(TAG, "=== Loading $NUM_FRAMES PNG frames from assets/frames/ ===")
        val rgbMats = loadFramesFromPNGs(NUM_FRAMES)

        assertEquals(
            "Expected exactly $NUM_FRAMES PNG frames in assets/frames/, got ${rgbMats.size}.",
            NUM_FRAMES,
            rgbMats.size
        )
        Log.i(TAG, "Loaded ${rgbMats.size} frames: ${rgbMats[0].cols()}×${rgbMats[0].rows()}")

        // ── Step 2: Load Haar cascade from res/raw → filesDir ─────────────────
        val cascadeFile = copyCascadeToFilesDir()
        val cascade = CascadeClassifier(cascadeFile.absolutePath)
        check(!cascade.empty()) {
            "CascadeClassifier is empty after loading from ${cascadeFile.absolutePath}. " +
                    "Verify haarcascade_frontalface_default.xml is at app/src/main/res/raw/."
        }
        Log.i(TAG, "Cascade loaded from: ${cascadeFile.absolutePath}")

        // ── Step 3a: Face detection — diagnostic only ─────────────────────────
        val preprocessor = Preprocessor(cascade)
        val detectedFaceBox = preprocessor.detectFaceBox(rgbMats[0])
        val ktFaceBox = intArrayOf(
            detectedFaceBox.x, detectedFaceBox.y,
            detectedFaceBox.width, detectedFaceBox.height
        )
        Log.i(TAG, "=== FACE BOX DIAGNOSTIC ===")
        Log.i(TAG, "  Python reference:  x=${REF_FACE_BOX[0]}, y=${REF_FACE_BOX[1]}, w=${REF_FACE_BOX[2]}, h=${REF_FACE_BOX[3]}")
        Log.i(TAG, "  Kotlin detected:   x=${ktFaceBox[0]}, y=${ktFaceBox[1]}, w=${ktFaceBox[2]}, h=${ktFaceBox[3]}")
        
        // ── Step 3b: Crop & resize using the PYTHON REFERENCE face box ────────
        val refBox = Rect(REF_FACE_BOX[0], REF_FACE_BOX[1], REF_FACE_BOX[2], REF_FACE_BOX[3])
        val croppedFrames = preprocessor.cropAndResize(rgbMats, refBox)
        
        // ── Step 3c: Diff-normalize ────────────────────────────────────────────
        val diffNormFrames = Preprocessor.diffNormalize(croppedFrames)

        // Release OpenCV Mat objects.
        rgbMats.forEach { it.release() }

        // ── Step 4: Extract probe values ──────────────────────────────────────
        val ktFrame0Pixel0RGB = floatArrayOf(
            croppedFrames[0][0],  // R
            croppedFrames[0][1],  // G
            croppedFrames[0][2]   // B
        )
        val frame5Base = 100 * 3
        val ktFrame5Pixel100RGB = floatArrayOf(
            diffNormFrames[5][frame5Base + 0],  // R
            diffNormFrames[5][frame5Base + 1],  // G
            diffNormFrames[5][frame5Base + 2]   // B
        )
        val ktFrame19Pixel0RGB = floatArrayOf(
            diffNormFrames[19][0],
            diffNormFrames[19][1],
            diffNormFrames[19][2]
        )
        val ktFrame20Pixel0RGB = floatArrayOf(
            diffNormFrames[20][0],
            diffNormFrames[20][1],
            diffNormFrames[20][2]
        )
        // Probe F: std of the NORMALIZED diff frames (first 20, excluding the duplicated
        // padding frame at index 20). This matches gen_reference.py's probe:
        //   diff = diff / (np.std(diff) + 1e-7)   # normalizes in-place (20 frames)
        //   ...pad to 21...
        //   "diffnorm_global_std_used": float(np.std(diff))  # std of normalized 20 frames ≈ 1.0
        val ktGlobalStd = computeNormalizedDiffStd(diffNormFrames)

        // ── Step 5: Log full comparison table ─────────────────────────────────
        val lines = buildComparisonTable(
            ktFaceBox, ktFrame0Pixel0RGB, ktFrame5Pixel100RGB,
            ktFrame19Pixel0RGB, ktFrame20Pixel0RGB, ktGlobalStd
        )
        lines.forEach { Log.i(TAG, it) }

        // ── Step 6: Assertions ────────────────────────────────────────────────
        Log.i(TAG, "=== ASSERTIONS ===")
        logFaceBoxDiagnostic(ktFaceBox)
        assertRGB("frame0_pixel0_rgb", REF_FRAME0_PIXEL0_RGB, ktFrame0Pixel0RGB)
        assertRGB("diffnorm_frame5_pixel100_rgb", REF_DIFFNORM_FRAME5_PIXEL100_RGB, ktFrame5Pixel100RGB)
        assertRGB("diffnorm_frame19_pixel0_rgb", REF_DIFFNORM_FRAME19_PIXEL0_RGB, ktFrame19Pixel0RGB)
        assertRGB("diffnorm_frame20_pixel0_rgb", REF_DIFFNORM_FRAME20_PIXEL0_RGB, ktFrame20Pixel0RGB)
        assertEquals(
            "diffnorm_global_std", REF_GLOBAL_STD, ktGlobalStd, TOLERANCE
        )
        Log.i(TAG, "=== ALL ASSERTIONS PASSED ===")
    }

    // ──────────────────────────────────────────────────────────────────────────
    // Helper: Load N PNG frames from androidTest assets/frames/ into RGB Mats
    // ──────────────────────────────────────────────────────────────────────────

    /**
     * Loads [count] PNG files from the androidTest APK's `assets/frames/` directory
     * (frame_00.png … frame_20.png) into 3-channel RGB OpenCV Mats.
     *
     * Each PNG was saved by Python's `cv2.imwrite(path, bgr_frame)` where `bgr_frame`
     * is the raw frame from `cv2.VideoCapture`. cv2.imwrite stores the pixel data in
     * RGB order inside the PNG file. When loaded by Android's
     * `BitmapFactory.decodeStream`, the result is an ARGB_8888 Bitmap with the visual
     * RGB colors intact. Then:
     *   - `Utils.bitmapToMat(bitmap, mat)` → 4-channel BGRA Mat
     *   - `Imgproc.cvtColor(bgraMat, rgbMat, COLOR_BGRA2RGB)` → 3-channel RGB Mat
     *
     * This chain is byte-for-byte equivalent to Python's:
     *   `cv2.cvtColor(bgr_frame, cv2.COLOR_BGR2RGB)` → RGB array
     */
    private fun loadFramesFromPNGs(count: Int): List<Mat> {
        val mats = mutableListOf<Mat>()
        for (i in 0 until count) {
            val name = "frames/frame_%02d.png".format(i)
            val bitmap: Bitmap = testContext.assets.open(name).use { stream ->
                BitmapFactory.decodeStream(stream)
                    ?: error("BitmapFactory.decodeStream returned null for asset: $name")
            }

            // ── Channel order analysis ────────────────────────────────────────────
            // cv2.imwrite(path, bgr_frame) saved BGR channels as-is to the PNG file:
            //   PNG R-slot = bgr.ch0 = Blue_visual = 234
            //   PNG G-slot = bgr.ch1 = Green       = 229
            //   PNG B-slot = bgr.ch2 = Red_visual  = 226
            //
            // BitmapFactory.decodeStream(PNG) gives ARGB_8888 Bitmap with visual-correct
            // pixels (Bitmap.R=234, G=229, B=226). Android ARGB_8888 stores bytes in
            // memory as RGBA: [R_visual=234, G=229, B_visual=226, A=255].
            //
            // Utils.bitmapToMat() reads those 4 bytes and creates a 4-channel Mat
            // treating them as BGRA: ch0=234(named B, is visual R), ch1=229(G),
            // ch2=226(named R, is visual B), ch3=255(A). The BGRA semantic label is
            // WRONG for this memory layout.
            //
            // COLOR_BGRA2RGB would swap ch0↔ch2 → output [226, 229, 234] = [B,G,R] ✗
            //
            // COLOR_BGRA2BGR just DROPS the alpha channel, preserving byte order:
            //   output ch0=234(R_visual), ch1=229(G), ch2=226(B_visual) ✓
            //
            // This matches Python's: cv2.cvtColor(bgr_frame, cv2.COLOR_BGR2RGB)
            //   → [R=234, G=229, B=226] byte-for-byte.
            //
            // Preprocessor.detectFaceBox applies COLOR_RGB2GRAY:
            //   0.299*ch0 + 0.587*ch1 + 0.114*ch2 = 0.299*234 + 0.587*229 + 0.114*226
            //   ≈ 230 ← same as Python ✓
            val rgbMat = Mat()
            val bgraMat = Mat()
            Utils.bitmapToMat(bitmap, bgraMat)
            bitmap.recycle()
            // Drop alpha only — do NOT swap ch0 and ch2.
            Imgproc.cvtColor(bgraMat, rgbMat, Imgproc.COLOR_BGRA2BGR)
            bgraMat.release()

            mats.add(rgbMat)
            if (i == 0) {
                Log.i(TAG, "PNG frame 0: ${rgbMat.cols()}×${rgbMat.rows()}, " +
                        "channels=${rgbMat.channels()}, type=${rgbMat.type()}")
            }
        }
        Log.i(TAG, "Loaded $count PNG frames from assets/frames/")
        return mats
    }


    // ──────────────────────────────────────────────────────────────────────────
    // Helper: Copy Haar cascade XML to filesDir for CascadeClassifier
    // ──────────────────────────────────────────────────────────────────────────

    /**
     * CascadeClassifier requires a real filesystem path (not an asset InputStream).
     * We copy the XML from res/raw to the app's internal files directory and return
     * the File object pointing to the copy.
     */
    private fun copyCascadeToFilesDir(): File {
        // Cascade XML is in the MAIN app's res/raw, so use targetContext.
        val outFile = File(targetContext.filesDir, "haarcascade_frontalface_default.xml")
        if (outFile.exists()) {
            Log.i(TAG, "Cascade already in filesDir: ${outFile.absolutePath}")
            return outFile
        }

        val rawId = targetContext.resources.getIdentifier(
            "haarcascade_frontalface_default", "raw", targetContext.packageName
        )
        check(rawId != 0) {
            "Resource raw/haarcascade_frontalface_default not found in package ${targetContext.packageName}. " +
                    "Ensure the XML is at app/src/main/res/raw/haarcascade_frontalface_default.xml"
        }

        targetContext.resources.openRawResource(rawId).use { input ->
            FileOutputStream(outFile).use { output ->
                input.copyTo(output)
            }
        }
        Log.i(TAG, "Cascade copied to filesDir: ${outFile.absolutePath} (${outFile.length()} bytes)")
        return outFile
    }

    // ──────────────────────────────────────────────────────────────────────────
    // Helper: Re-compute global std (to match gen_reference.py's probe)
    // ──────────────────────────────────────────────────────────────────────────

    /**
     * Computes the population std of the NORMALIZED inter-frame diff frames,
     * matching Python's probe `"diffnorm_global_std_used": float(np.std(diff))`.
     *
     * In gen_reference.py the sequence is:
     *   1. Compute raw diff[j] = (next-curr)/(next+curr+1e-7)  for j in 0..19
     *   2. diff = diff / (np.std(diff) + 1e-7)   <-- normalizes IN-PLACE
     *   3. diff[isnan] = 0
     *   4. Pad to 21 frames by appending last diff frame
     *   5. Record np.std(diff)  <-- std of the 20 normalized frames, NOT the 21-frame padded result
     *
     * So we compute population std over [diffNormFrames[0] .. diffNormFrames[n-2]],
     * i.e., ALL frames EXCEPT the last (which is the duplicated padding frame).
     * This should be ≈ 1.0 (it's the std of data that was divided by its own std).
     *
     * @param normalizedFrames The 21-element array returned by [Preprocessor.diffNormalize].
     */
    private fun computeNormalizedDiffStd(normalizedFrames: Array<FloatArray>): Float {
        // Use only frames 0..n-2 (20 frames), excluding the duplicated padding frame.
        val framesToUse = normalizedFrames.size - 1  // 20
        val elemCount = normalizedFrames[0].size
        val totalElements = framesToUse.toLong() * elemCount
        var sum = 0.0
        var sumSq = 0.0
        for (j in 0 until framesToUse) {
            for (v in normalizedFrames[j]) {
                sum += v
                sumSq += v.toDouble() * v.toDouble()
            }
        }
        val mean = sum / totalElements
        val variance = (sumSq / totalElements) - (mean * mean)
        return Math.sqrt(variance).toFloat()
    }

    // ──────────────────────────────────────────────────────────────────────────
    // Helper: Build side-by-side comparison table for logging
    // ──────────────────────────────────────────────────────────────────────────

    private fun buildComparisonTable(
        ktFaceBox: IntArray,
        ktFrame0Pixel0: FloatArray,
        ktFrame5Pixel100: FloatArray,
        ktFrame19Pixel0: FloatArray,
        ktFrame20Pixel0: FloatArray,
        ktGlobalStd: Float
    ): List<String> {
        val lines = mutableListOf<String>()

        lines += "╔══════════════════════════════════════════════════════════════════════════╗"
        lines += "║         KOTLIN vs PYTHON PREPROCESSOR VALIDATION COMPARISON TABLE        ║"
        lines += "╠══════════════════════════════════════╦══════════════════════════════════╣"
        lines += "║ Probe                                ║ Python Ref  | Kotlin    | Δ       ║"
        lines += "╠══════════════════════════════════════╬══════════════════════════════════╣"

        // Face box
        val faceBoxMatch = ktFaceBox.zip(REF_FACE_BOX.toList()).all { (k, r) -> k == r }
        lines += "║ face_box [x,y,w,h]                   ║ ${REF_FACE_BOX.toList()}  →  ${ktFaceBox.toList()}  ${if (faceBoxMatch) "✓" else "✗"} ║"

        // RGB probes
        fun rgbRow(name: String, ref: FloatArray, kt: FloatArray): String {
            val diffs = ref.zip(kt.toList()).map { (r, k) -> Math.abs(r - k) }
            val maxDiff = diffs.maxOrNull() ?: 0f
            val ok = maxDiff < TOLERANCE
            return "║ $name".padEnd(38) + "║ R:${ref[0].fmt()} G:${ref[1].fmt()} B:${ref[2].fmt()} ║"
        }

        // Simplified logging (Log.i has line-length limits)
        fun addRGBProbe(name: String, ref: FloatArray, kt: FloatArray) {
            val diffs = ref.zip(kt.toList()).map { (r, k) -> Math.abs(r - k) }
            val maxDiff = diffs.maxOrNull() ?: 0f
            val ok = maxDiff < TOLERANCE
            val status = if (ok) "PASS ✓" else "FAIL ✗"
            lines += "║ $name [$status]"
            lines += "║   Python: R=${ref[0].fmt6()}  G=${ref[1].fmt6()}  B=${ref[2].fmt6()}"
            lines += "║   Kotlin: R=${kt[0].fmt6()}   G=${kt[1].fmt6()}   B=${kt[2].fmt6()}"
            lines += "║   Δ:      R=${diffs[0].fmt6()}  G=${diffs[1].fmt6()}  B=${diffs[2].fmt6()}  (max_Δ=${maxDiff.fmt6()}, tol=$TOLERANCE)"
        }

        fun addScalarProbe(name: String, ref: Float, kt: Float) {
            val diff = Math.abs(ref - kt)
            val ok = diff < TOLERANCE
            val status = if (ok) "PASS ✓" else "FAIL ✗"
            lines += "║ $name [$status]"
            lines += "║   Python: ${ref.fmt6()}"
            lines += "║   Kotlin: ${kt.fmt6()}"
            lines += "║   Δ:      ${diff.fmt6()} (tol=$TOLERANCE)"
        }

        addRGBProbe("frame0_pixel0_rgb", REF_FRAME0_PIXEL0_RGB, ktFrame0Pixel0)
        addRGBProbe("diffnorm_frame5_pixel100_rgb", REF_DIFFNORM_FRAME5_PIXEL100_RGB, ktFrame5Pixel100)
        addRGBProbe("diffnorm_frame19_pixel0_rgb", REF_DIFFNORM_FRAME19_PIXEL0_RGB, ktFrame19Pixel0)
        addRGBProbe("diffnorm_frame20_pixel0_rgb", REF_DIFFNORM_FRAME20_PIXEL0_RGB, ktFrame20Pixel0)
        addScalarProbe("diffnorm_global_std", REF_GLOBAL_STD, ktGlobalStd)

        lines += "╚══════════════════════════════════════════════════════════════════════════╝"

        // Add face box detail separately
        lines += "║ face_box: Python=[${REF_FACE_BOX.joinToString(",")}]  Kotlin=[${ktFaceBox.joinToString(",")}]  Match=${ktFaceBox.contentEquals(REF_FACE_BOX)}"

        return lines
    }

    // ──────────────────────────────────────────────────────────────────────────
    // Helper: Assertions
    // ──────────────────────────────────────────────────────────────────────────

    /**
     * Soft face-box diagnostic: logs differences but does NOT assert.
     *
     * Cascade coordinate jitter of ±3–5px between desktop OpenCV (Python) and
     * the Android AAR is expected and is NOT a logic error. What matters for
     * signal quality is that w and h are similar (same scale crop). The pixel
     * probe assertions use the Python reference box directly, so this function
     * is purely informational.
     *
     * A WARNING is emitted if w or h differs by more than 10px, which would
     * suggest a different face scale was detected.
     */
    private fun logFaceBoxDiagnostic(kt: IntArray) {
        val ref = REF_FACE_BOX
        val labels = arrayOf("x", "y", "w", "h")
        for (i in 0..3) {
            val diff = Math.abs(kt[i] - ref[i])
            val label = labels[i]
            val verdict = when {
                i < 2 && diff <= 5  -> "OK (position jitter ≤5px)"    // x, y: jitter expected
                i < 2 && diff <= 15 -> "WARN (position jitter ${diff}px)"  // x, y: larger shift
                i < 2               -> "ERROR (large position shift ${diff}px)"
                diff <= 5           -> "OK"                              // w, h: size match
                diff <= 15          -> "WARN (size differs ${diff}px)"
                else                -> "ERROR (wrong face scale, Δ=${diff}px)"
            }
            Log.i(TAG, "  face_box.$label: Python=${ref[i]}, Kotlin=${kt[i]}, Δ=$diff — $verdict")
        }
        // Hard-fail only if w or h differ by more than 20px (wrong face detected).
        val wDiff = Math.abs(kt[2] - ref[2])
        val hDiff = Math.abs(kt[3] - ref[3])
        if (wDiff > 20 || hDiff > 20) {
            throw AssertionError(
                "face_box size mismatch is too large (w_diff=$wDiff, h_diff=$hDiff): " +
                "likely a different face was detected. " +
                "Python=[${ref.joinToString(",")}], Kotlin=[${kt.joinToString(",")}]. " +
                "Check that only one face is visible in frame 0 of the test video."
            )
        }
    }

    private fun assertRGB(name: String, ref: FloatArray, kt: FloatArray) {
        val channels = arrayOf("R", "G", "B")
        for (i in 0..2) {
            val diff = Math.abs(ref[i] - kt[i])
            assertEquals(
                "$name.${channels[i]}: Python=${ref[i]}, Kotlin=${kt[i]}, Δ=$diff. " +
                        "Exceeds tolerance=$TOLERANCE. Possible causes: wrong channel order " +
                        "(BGR/RGB swap), codec color-space mismatch, or wrong pixel indexing.",
                ref[i], kt[i], TOLERANCE
            )
        }
    }

    // ──────────────────────────────────────────────────────────────────────────
    // Extension: Format floats for logging
    // ──────────────────────────────────────────────────────────────────────────

    private fun Float.fmt(): String = "%.4f".format(this)
    private fun Float.fmt6(): String = "%.6f".format(this)
